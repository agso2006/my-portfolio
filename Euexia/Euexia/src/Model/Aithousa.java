package Model;

public class Aithousa {
    
    private int number;
    private int floor;

    public Aithousa() {
    }

    public Aithousa(int number, int floor) {
        this.number = number;
        this.floor = floor;
    }

    public int getNumber() {
        return number;
    }


    public int getFloor() {
        return floor;
    }

 
    
    
}
